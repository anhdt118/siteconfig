function render_origin($content) {
    $content = str_replace(array('link'), 'div', $content);
    $content = preg_replace(array('@<style[^>]*?>.*?</style>@siu','@<script[^>]*?.*?</script>@siu'), '', $content);
    $content = str_replace(array('window', 'document', 'location'), '', $content);
    return $content;
}

